/**
 * op_add - A function that adds two integer numbers
 * @a: the first integer number
 * @b: the second integer number
 *
 * Return: returns the result of the calculation
**/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - A function that subtracts two integer numbers
 * @a: the first integer number
 * @b: the second integer number
 *
 * Return: returns the result of the calculation
**/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A functions that Multiplies two integer numbers
 * @a: the first integer number
 * @b: the second integer number
 *
 * Return: returns the result of the calculation
**/

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - A function that Divides two integer numbers
 * @a: the first integer number
 * @b: the second integer number
 *
 * Return: returns the result of the calculation
**/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A function that finds the Modules of two integer numbers
 * @a: the first integer number
 * @b: the second integer number
 *
 * Return: returns the result of the calculation
**/
int op_mod(int a, int b)
{
	return (a % b);
}
